function calculateEntropy(str) {
  const freq = {};
  for (let i = 0; i < str.length; i++) {
    freq[str[i]] = (freq[str[i]] || 0) + 1;
  }
  
  let entropy = 0;
  for (let char in freq) {
    const p = freq[char] / str.length;
    entropy -= p * Math.log2(p);
  }
  
  return entropy * str.length;
}